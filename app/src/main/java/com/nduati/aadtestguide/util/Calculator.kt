package com.nduati.aadtestguide.util

class Calculator {
    fun add(x : Int, y : Int) : Int{
        return x + y
    }

    fun subtract(x : Int, y : Int) : Int{
        return x - y
    }

    fun divid(x : Int, y : Int) : Int{
        return x/y
    }

    fun multi(x: Int, y: Int) : Int{
        return x*y
    }
}